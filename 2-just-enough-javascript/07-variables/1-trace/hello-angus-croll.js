'use strict';

// declare a variable first and initialize it to the value 'angus'
let first = 'angus';
// read the variable first
console.log(first);
// declare a variable last and initialize it to the value 'croll'
let last = 'croll';
// read the variable last
console.log(last);
// declare a variable greeting and initialize it to the value 'hello angus croll'
let greeting = 'hello ' + first + ' ' + last;

// read the variable greeting
console.log(greeting);
